def clean_mask(mask):
    """Çıkartma kenarlarını pürüzsüzleştiren o 'süper' fonksiyon."""
    mask_uint8 = (mask * 255).astype(np.uint8)
    contours, _ = cv2.findContours(mask_uint8, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    if not contours: return mask_uint8
    c = max(contours, key=cv2.contourArea)
    new_mask = np.zeros_like(mask_uint8)
    cv2.drawContours(new_mask, [c], -1, 255, thickness=cv2.FILLED)
    new_mask = cv2.GaussianBlur(new_mask, (5, 5), 0)
    _, new_mask = cv2.threshold(new_mask, 127, 255, cv2.THRESH_BINARY)
    return new_mask

def validate_object(image_rgb, target_name):
    """Yapay Zeka ile nesne kontrolü yapan yeni katman."""
    preprocess = transforms.Compose([
        transforms.Resize(256),
        transforms.CenterCrop(224),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]),
    ])

    img_pil = Image.fromarray(image_rgb)
    img_t = preprocess(img_pil).unsqueeze(0).to(device)

    with torch.no_grad():
        output = classifier(img_t)

    _, indices = torch.sort(output, descending=True)
    # İlk 10 tahmini kontrol et (Daha esnek olması için)
    top10_predictions = [imagenet_labels[idx].lower() for idx in indices[0][:10]]

    target = target_name.lower()
    # Tahminlerin içinde hedef kelime geçiyor mu?
    is_correct = any(target in p or p in target for p in top10_predictions)

    return is_correct, top10_predictions[0]

