@app.route('/process', methods=['POST'])
def process_image():
    try:
        file = request.files['image']
        target_name = request.form.get('target_object', 'object')

        file_bytes = np.frombuffer(file.read(), np.uint8)
        image = cv2.imdecode(file_bytes, cv2.IMREAD_COLOR)
        image_rgb = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)

        # A. ÇIKARTMA YAPMA (SAM MANTIĞI)
        h, w = image_rgb.shape[:2]
        box_w, box_h = w * 0.4, h * 0.4
        x_min, y_min = int((w - box_w) / 2), int((h - box_h) / 2)
        x_max, y_max = int((w + box_w) / 2), int((h + box_h) / 2)
        input_box = np.array([x_min, y_min, x_max, y_max])

        predictor.set_image(image_rgb)
        masks, scores, _ = predictor.predict(box=input_box, multimask_output=True)
        best_mask = masks[np.argmax(scores)]

        cleaned_mask_255 = clean_mask(best_mask)
        final_mask_bool = cleaned_mask_255 > 0

        coords = np.column_stack(np.where(final_mask_bool))
        if coords.size == 0:
            return jsonify({'status': 'error', 'message': 'Nesne algılanamadı'})

        y_min_c, x_min_c = coords.min(axis=0)
        y_max_c, x_max_c = coords.max(axis=0)

        transparent_image = np.zeros((h, w, 4), dtype=np.uint8)
        transparent_image[:, :, :3] = image
        transparent_image[:, :, 3] = cleaned_mask_255

        cropped_sticker = transparent_image[y_min_c:y_max_c+1, x_min_c:x_max_c+1]

        # Boyutlandırma ve Base64
        max_dim = 800
        ch, cw = cropped_sticker.shape[:2]
        scale = max_dim / max(ch, cw)
        if scale < 1:
            cropped_sticker = cv2.resize(cropped_sticker, (int(cw * scale), int(ch * scale)))

        _, buffer = cv2.imencode('.png', cropped_sticker)
        encoded_image = base64.b64encode(buffer).decode('utf-8')

        # B. DOĞRULAMA (YENİ MANTIK)
        # Orijinal resim üzerinden AI'ya ne olduğunu soruyoruz
        is_correct, detected_name = validate_object(image_rgb, target_name)

        return jsonify({
            'status': 'success',
            'image': encoded_image,
            'is_correct': is_correct,
            # 'detected_object' alanını silebiliriz veya boş gönderebiliriz
            'message': 'Çıkartma oluşturuldu!'
        })

    except Exception as e:
        return jsonify({'status': 'error', 'message': str(e)})

